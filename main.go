type myMap struct {
	key   int
	value int
}

func kWeakestRows(mat [][]int, k int) []int {
	var my []myMap

	for i, row := range mat {
		count := 0
		for _, val := range row {
			count += val
		}
		my = append(my, myMap{i, count})
	}

	sort.Slice(my, func(i, j int) bool {
		if my[i].value == my[j].value {
			return my[i].key < my[j].key
		}
		return my[i].value < my[j].value
	})

	result := make([]int, k)
	for i := 0; i < k; i++ {
		result[i] = my[i].key
	}

	return result
}
